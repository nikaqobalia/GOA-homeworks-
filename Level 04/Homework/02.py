#შექმენით ორი ცვლადი, თითოეულში შეინახეთ ნებისმიერი რიცხვი. თქვენი დავალებაა, რომ არითმეტიკული მოქმედებების შედეგი დაბეჭდოთ ამ რიცხვებს შორის - ეს მოქმედებებია +, -, *, /
#პირველი ცვლადი
num_1 = 55
#მეორე ცვლადი
num_2 = 45
#პირველ ცვლადს დავუმატე მეორე ცვლადი
print(num_1 + num_2)
#პირველ ცვლადს გამოვაკელი მეორე ცვლადი
print(num_1 - num_2)
#პირველი ცვლადი გავამრავლე მეორე ცვლადზე
print(num_1 * num_2)
#პირველი ცვლადი გავყავი მეორე ცვლადზე
print(num_1 / num_2)